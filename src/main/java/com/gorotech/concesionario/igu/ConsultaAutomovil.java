    private void cargarTabla() {
        
        //Hace que la tabla no sea editable sin utilizar el boton editar
        DefaultTableModel modeloTabla = new DefaultTableModel(){
            //Ya existe este metodo por eso lo sobreescribo
            @Override
            public boolean isCellEditable(int row, int column){
                return false;
            }
        };
        
        //Ponemos titulos a las columnas de la tabla
        String titulos [] = {"Id","Modelo","Marca","Motor","Color","Patente","Puertas"};
        modeloTabla.setColumnIdentifiers(titulos);
        
        //traer los autos desde la base de datos
        List <Automovil> listaAutomoviles = control.traerAutos();
        
        //Setear los datos en la tabla
        if(listaAutomoviles!= null){
            for(Automovil auto : listaAutomoviles){
                Object[] object = {auto.getId(), auto.getModelo(),auto.getMarca(),auto.getModelo(),auto.getColor(),auto.getPatente(),auto.getCantPuertas()};
                modeloTabla.addRow(object);
            }
        }
        tablaAutos.setModel(modeloTabla);
    }
